package loops;


public class EnhancedForLoops 
{
	public static void main(String[] args) {
	int [] numbers= {10,20,30,40,50,60};
	
	for(int x:numbers)
	{
		System.out.println(x);
		System.out.println("");
	}
	
	String [] names= {"Sharad","Henil","Shubh"};
	
	for(String Y:names)
	{
		System.out.println(Y);
		System.out.println("");		
	}

	}
}
